function qualDia(dia) {
  switch (dia) {
    case 1:
      console.log('Fim de semana');
      break;
    case 2:
      console.log('Dia útil');
      break;
    case 3:
      console.log('Dia útil');
      break;
    case 4:
      console.log('Dia útil');
      break;
    case 5:
      console.log('Dia útil');
      break;
    case 6:
      console.log('Dia útil');
      break;
    case 7:
      console.log('Fim de semana');
      break;
    default:
      console.log('Dia inválido');
      break;
  }
}

qualDia(1);
qualDia(2);
qualDia(3);
qualDia(4);
qualDia(5);
qualDia(6);
qualDia(7);
qualDia(8);
qualDia(-1);
